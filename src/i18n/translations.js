export function translate(language, key, values = {}) {
  const activeTranslations = translations[language] || translations.fr;
  const template = activeTranslations[key] ?? translations.fr[key] ?? key;

  if (typeof template !== "string") {
    return template;
  }

  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template
  );
}

export function getLocalizedProductName(product, language) {
  if (language === "ar") {
    return product?.name_ar || product?.nameAr || product?.name || "";
  }

  return product?.name || "";
}

export function getLocalizedProductDescription(product, language) {
  if (language === "ar") {
    return (
      product?.description_ar ||
      product?.descriptionAr ||
      product?.description ||
      ""
    );
  }

  return product?.description || "";
}
